package Loops;
public class Reverse3vartri {
    public static void main(String[] args) {
        int n = 0;
        for(int i = 1; i <= 4 ; i++){
            for (int k = 1 ; k<=i ; k++){
                n++;
                System.out.print(n+" ");
            }
            System.out.println();
        }
    }
    
}
